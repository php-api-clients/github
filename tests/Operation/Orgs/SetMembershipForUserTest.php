    public function call_httpCode_200_requestContentType_application_json_responseContentType_application_json_zero(): void
    {
        $response = new Response(200, ['Content-Type' => 'application/json'], Schema\OrgMembership::SCHEMA_EXAMPLE_DATA);
        $auth     = $this->prophesize(AuthenticationInterface::class);
        $auth->authHeader(Argument::any())->willReturn('Bearer beer')->shouldBeCalled();
        $browser = $this->prophesize(Browser::class);
        $browser->withBase(Argument::any())->willReturn($browser->reveal());
        $browser->withFollowRedirects(Argument::any())->willReturn($browser->reveal());
        $browser->request('PUT', '/orgs/generated/memberships/generated', Argument::type('array'), Schema\Orgs\SetMembershipForUser\Request\ApplicationJson::SCHEMA_EXAMPLE_DATA)->willReturn(resolve($response))->shouldBeCalled();
        $client = new Client($auth->reveal(), $browser->reveal());
        $result = $client->call(Operation\Orgs\SetMembershipForUser::OPERATION_MATCH, (static function (array $data): array {
            $data['org']      = 'generated';
            $data['username'] = 'generated';

            return $data;
        })(json_decode(Schema\Orgs\SetMembershipForUser\Request\ApplicationJson::SCHEMA_EXAMPLE_DATA, true)));
    }

