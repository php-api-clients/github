        $result = $client->call(Operation\Actions\ListJobsForWorkflowRunAttempt::OPERATION_MATCH, (static function (array $data): array {
            $data['owner']          = 'generated';
            $data['repo']           = 'generated';
            $data['run_id']         = 6;
            $data['attempt_number'] = 14;
            $data['per_page']       = 8;
            $data['page']           = 1;

            return $data;
        })([]));
