        $response = new Response(200, ['Content-Type' => 'application/json'], json_encode(json_decode(Schema\Hovercard::SCHEMA_EXAMPLE_DATA, true)));
        $auth     = $this->prophesize(AuthenticationInterface::class);
        $auth->authHeader(Argument::any())->willReturn('Bearer beer')->shouldBeCalled();
        $browser = $this->prophesize(Browser::class);
        $browser->withBase(Argument::any())->willReturn($browser->reveal());
        $browser->withFollowRedirects(Argument::any())->willReturn($browser->reveal());
        $browser->request('GET', '/users/generated/hovercard?subject_type=generated&subject_id=generated', Argument::type('array'), Argument::any())->willReturn(resolve($response))->shouldBeCalled();
        $client = new Client($auth->reveal(), $browser->reveal());
        $result = $client->call(Operation\Users\GetContextForUser::OPERATION_MATCH, (static function (array $data): array {
            $data['username']     = 'generated';
            $data['subject_type'] = 'generated';
            $data['subject_id']   = 'generated';

            return $data;
        })([]));
    }

