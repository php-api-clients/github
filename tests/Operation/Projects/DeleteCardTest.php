        $response = new Response(403, ['Content-Type' => 'application/json'], Schema\Operations\Projects\DeleteCard\Response\ApplicationJson\Forbidden::SCHEMA_EXAMPLE_DATA);
        $auth     = $this->prophesize(AuthenticationInterface::class);
        $auth->authHeader(Argument::any())->willReturn('Bearer beer')->shouldBeCalled();
        $browser = $this->prophesize(Browser::class);
        $browser->withBase(Argument::any())->willReturn($browser->reveal());
        $browser->withFollowRedirects(Argument::any())->willReturn($browser->reveal());
        $browser->request('DELETE', '/projects/columns/cards/7', Argument::type('array'), Argument::any())->willReturn(resolve($response))->shouldBeCalled();
        $client = new Client($auth->reveal(), $browser->reveal());
        $result = $client->call(Operation\Projects\DeleteCard::OPERATION_MATCH, (static function (array $data): array {
            $data['card_id'] = 7;

            return $data;
        })([]));
    }

