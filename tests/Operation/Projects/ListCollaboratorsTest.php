        $auth     = $this->prophesize(AuthenticationInterface::class);
        $auth->authHeader(Argument::any())->willReturn('Bearer beer')->shouldBeCalled();
        $browser = $this->prophesize(Browser::class);
        $browser->withBase(Argument::any())->willReturn($browser->reveal());
        $browser->withFollowRedirects(Argument::any())->willReturn($browser->reveal());
        $browser->request('GET', '/projects/10/collaborators?affiliation=generated&per_page=8&page=4', Argument::type('array'), Argument::any())->willReturn(resolve($response))->shouldBeCalled();
        $client = new Client($auth->reveal(), $browser->reveal());
        $result = $client->call(Operation\Projects\ListCollaborators::OPERATION_MATCH, (static function (array $data): array {
            $data['project_id']  = 10;
            $data['affiliation'] = 'generated';
            $data['per_page']    = 8;
            $data['page']        = 4;

            return $data;
        })([]));
    }

