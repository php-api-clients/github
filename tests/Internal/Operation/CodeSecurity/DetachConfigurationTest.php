    {
        self::expectException(ErrorSchemas\BasicError::class);
        $response = new Response(400, ['Content-Type' => 'application/json'], json_encode(json_decode(Schema\BasicError::SCHEMA_EXAMPLE_DATA, true)));
        $auth     = $this->prophesize(AuthenticationInterface::class);
        $auth->authHeader(Argument::any())->willReturn('Bearer beer')->shouldBeCalled();
        $browser = $this->prophesize(Browser::class);
        $browser->withBase(Argument::any())->willReturn($browser->reveal());
        $browser->withFollowRedirects(Argument::any())->willReturn($browser->reveal());
        $browser->request('DELETE', '/orgs/generated/code-security/configurations/detach', Argument::type('array'), json_encode(json_decode(Schema\CodeSecurity\DetachConfiguration\Request\ApplicationJson::SCHEMA_EXAMPLE_DATA, true)))->willReturn(resolve($response))->shouldBeCalled();
        $client = new Client($auth->reveal(), $browser->reveal());
        $result = $client->call(Internal\Operation\CodeSecurity\DetachConfiguration::OPERATION_MATCH, (static function (array $data): array {
            $data['org'] = 'generated';

            return $data;
        })(json_decode(Schema\CodeSecurity\DetachConfiguration\Request\ApplicationJson::SCHEMA_EXAMPLE_DATA, true)));
    }

